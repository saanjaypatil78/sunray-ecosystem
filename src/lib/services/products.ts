// Helper functions
export function getProductsByCategory(category: ProductCategory): Product[] {
  return PRODUCTS.filter(p => p.category === category && p.isActive);
}

export function getProductsBySource(source: ProductSource): Product[] {
  return PRODUCTS.filter(p => p.source === source && p.isActive);
}

export function getDailyDeals(): Product[] {
  return PRODUCTS.filter(p => p.isDailyDeal && p.isActive && p.dealEndTime && new Date(p.dealEndTime) > new Date());
}

export function getFeaturedProducts(): Product[] {
  return PRODUCTS.filter(p => p.isFeatured && p.isActive);
}

export function searchProducts(query: string): Product[] {
  const lowerQuery = query.toLowerCase();
  return PRODUCTS.filter(p => 
    p.isActive && (
      p.name.toLowerCase().includes(lowerQuery) ||
      p.keywords.some(k => k.toLowerCase().includes(lowerQuery)) ||
      p.description.toLowerCase().includes(lowerQuery) ||
      p.brand.toLowerCase().includes(lowerQuery)
    )
  );
}

export function getProductById(id: string): Product | undefined {
  return PRODUCTS.find(p => p.id === id);
}

export function getProductsByVendor(vendorId: string): Product[] {
  return PRODUCTS.filter(p => p.vendorId === vendorId && p.isActive);
}

// Calculate deal discount
export function calculateDealDiscount(product: Product): number {
  if (!product.isDailyDeal) return 0;
  return Math.round(((product.mrp - product.sellingPrice) / product.mrp) * 100);
}

// Check if deal is active
export function isDealActive(product: Product): boolean {
  return product.isDailyDeal && !!product.dealEndTime && new Date(product.dealEndTime) > new Date();
}
