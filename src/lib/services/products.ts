export interface Product {
  id: string;
  name: string;
  keywords: string[];
  description: string;
  shortDescription: string;
  category: ProductCategory;
  subcategory: string;
  brand: string;
  source: ProductSource;
  
  // Pricing
  mrp: number; // Maximum Retail Price in INR
  sellingPrice: number; // Current selling price in INR
  costPrice: number; // Vendor cost price in INR
  margin: number; // Profit margin percentage
  
  // Images
  images: string[];
  thumbnail: string;
  
  // Inventory
  stock: number;
  sku: string;
  barcode?: string;
  
  // Ratings & Reviews
  rating: number;
  reviewCount: number;
  reviews: ProductReview[];
  
  // Sales Data
  totalSold: number;
  weeklySold: number;
  dailySold: number;
  
  // Shipping
  weight: number; // in grams
  dimensions: { length: number; width: number; height: number }; // in cm
  freeShipping: boolean;
  deliveryDays: number;
  
  // Vendor
  vendorId: string;
  vendorName: string;
  vendorRating: number;
  
  // Status
  isActive: boolean;
  isFeatured: boolean;
  isDailyDeal: boolean;
  dealEndTime?: Date;
  
  // Timestamps
  createdAt: Date;
  updatedAt: Date;
}

